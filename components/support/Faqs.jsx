function Faqs() {
  const [accordion, setActiveAccordion] = useState(0);

  function toggleAccordion(index) {
    if (index === accordion) {
      setActiveAccordion(-1);
      return;
    }
    setActiveAccordion(index);
  }

  return (
    <>
      <div className="contanier">
        <div>
          <span className="accorian__title">FAQ's</span>
          <h1>Lets answer some of your questions</h1>
        </div>
        <div className="accordion__faq">
          {dataCollection.map((item, index) => (
            <div key={index} onClick={() => toggleAccordion(index)}>
              <div className="accordion__faq-heading">
                <h3 className={accordion === index ? "active" : ""}>
                  {item.question}
                </h3>
              </div>
              <div>
                {accordion === index ? (
                  <>
                    <span className="verticle">-</span>
                  </>
                ) : (
                  <>
                    <span className="verticle">+</span>
                  </>
                )}
              </div>
              <div>
                <p className={accordion === index ? "active" : "index"}>
                  {item.answer}
                </p>
              </div>
            </div>
          ))}
        </div>
      </div>
    </>
  );
}
export default Faqs;
